    private void AddbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddbtnMouseClicked
        if (Usernom.getText().isEmpty() || Userid.getText().isEmpty() || Userpass.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Remplis tous les champs !");
        }
        else{
            try {
                PreparedStatement add =con.prepareStatement("insert into USERRS values (?,?,?,?)");
                add.setInt(1, Integer.valueOf(Userid.getText()));
                add.setString(2, Usernom.getText());
                add.setString(3, Userpass.getText());
                add.setString(4, Usercat.getSelectedItem().toString());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Utilisatuer a ete ajouter avec succes");
                st.close();
                SelectUser();
                
                
                }
           catch(Exception e) {
               JOptionPane.showMessageDialog(this, "Utilisatuer a ete pas ajouter");
                }}
    }//GEN-LAST:event_AddbtnMouseClicked

